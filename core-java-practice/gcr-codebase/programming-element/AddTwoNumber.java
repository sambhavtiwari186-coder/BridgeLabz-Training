import java.util.*;
public class AddTwoNumber{
    public static void main(String[] args) {
        // Take two input
        Scanner sc= new Scanner(System.in);
        int n= sc.nextInt();
        int m= sc.nextInt();
        // Make an output variable 
        int ans=m+n;
        // Print the answer
        System.out.println(ans);
    }
}